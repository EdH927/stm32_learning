void max7219_write(uint8_t addr, uint8_t data) {
	uint16_t writeData = (addr << 8) | data;
	HAL_GPIO_WritePin(GPIOA, SPI1_CS_Pin, 0);
	HAL_SPI_Transmit(&hspi1, (uint8_t *) &writeData, 1, 100);
	HAL_GPIO_WritePin(GPIOA, SPI1_CS_Pin, 1);
}


void matrixInit(void) {
	max7219_write(0x09, 0); // no decode
	max7219_write(0x0a, 0x01); // 3/32 intensity
	max7219_write(0x0B, 0x07); // enable all columns
	max7219_write(0x0C, 0x01); // Normal mode
	max7219_write(0x0F, 0); // No demo
}
